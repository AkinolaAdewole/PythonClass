# Printing a message indicating this is a calculator program.
print("calculator program")

# Taking input from the user for the first number.
first_number = input("first number: ")

# Taking input from the user for the second number.
second_number = input("second number: ")

# Converting the input values (which are strings) to integers and adding them.
# The 'int()' function converts the string inputs to integers for arithmetic operations.
print(int(first_number) + int(second_number))  # Printing the result of the addition.
