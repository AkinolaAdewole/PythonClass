course = "Python for Beginners"
# upper(): Converts all characters in the string to uppercase.
print(course.upper());

# lower(): Converts all characters to lowercase.
print(course.lower());

# find(substring): Finds the index of the first occurrence of a substring; returns -1 if not found.
print(course.find("n"))

# replace(old, new): Replaces all occurrences of the old substring with a new one.
print(course.replace("for","4"))

# 'substring' in string: Checks if a substring exists in the string; returns True or False.
print('Python' in course)
