# Loop 1: Iterating over a range of numbers from 0 to 4 (5 is not included).
for number in range(5):
    print(number)
# Output:
# 0
# 1
# 2
# 3
# 4

# Loop 2: Assigning range(5) to a variable and iterating over it.
num1 = range(5)
for number in num1:
    print(number)
# Output:
# 0
# 1
# 2
# 3
# 4

# Loop 3: Iterating over a range from 5 to 9.
num1 = range(5, 10)
for number in num1:
    print(number)
# Output:
# 5
# 6
# 7
# 8
# 9

# Loop 4: Iterating over a range from 10 to 18, with a step of 2.
num1 = range(10, 20, 2)
for number in num1:
    print(number)
# Output:
# 10
# 12
# 14
# 16
# 18

