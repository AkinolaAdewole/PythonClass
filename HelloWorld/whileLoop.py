# The while loop continuously checks the condition before each iteration.
# If the condition is true, the code block inside the loop is executed.
# When the condition becomes false, the loop stops, and the program continues with the
# next section of code after the while block.

i = 1
while i <= 3 :
    print(i)
    i = i+1


j = 1
while j <= 3 :
    print(j * "*")
    j = j+1